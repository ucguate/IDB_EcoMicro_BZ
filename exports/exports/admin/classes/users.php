	// Export data in HTML/CSV/Word/Excel/Email/PDF format
	public function exportDocument($doc, $recordset, $startRec = 1, $stopRec = 1, $exportPageType = "")
	{
		if (!$recordset || !$doc)
			return;
		if (!$doc->ExportCustom) {

			// Write header
			$doc->exportTableHeader();
			if ($doc->Horizontal) { // Horizontal format, write header
				$doc->beginExportRow();
				if ($exportPageType == "view") {
					$doc->exportCaption($this->id);
					$doc->exportCaption($this->first_names);
					$doc->exportCaption($this->last_names);
					$doc->exportCaption($this->_email);
					$doc->exportCaption($this->password);
					$doc->exportCaption($this->user_level);
					$doc->exportCaption($this->created_at);
					$doc->exportCaption($this->updated_at);
				} else {
					$doc->exportCaption($this->id);
					$doc->exportCaption($this->first_names);
					$doc->exportCaption($this->last_names);
					$doc->exportCaption($this->_email);
					$doc->exportCaption($this->password);
					$doc->exportCaption($this->user_level);
					$doc->exportCaption($this->created_at);
					$doc->exportCaption($this->updated_at);
				}
				$doc->endExportRow();
			}
		}

		// Move to first record
		$recCnt = $startRec - 1;
		if (!$recordset->EOF) {
			$recordset->moveFirst();
			if ($startRec > 1)
				$recordset->move($startRec - 1);
		}
		while (!$recordset->EOF && $recCnt < $stopRec) {
			$recCnt++;
			if ($recCnt >= $startRec) {
				$rowCnt = $recCnt - $startRec + 1;

				// Page break
				if ($this->ExportPageBreakCount > 0) {
					if ($rowCnt > 1 && ($rowCnt - 1) % $this->ExportPageBreakCount == 0)
						$doc->exportPageBreak();
				}
				$this->loadListRowValues($recordset);

				// Render row
				$this->RowType = ROWTYPE_VIEW; // Render view
				$this->resetAttributes();
				$this->renderListRow();
				if (!$doc->ExportCustom) {
					$doc->beginExportRow($rowCnt); // Allow CSS styles if enabled
					if ($exportPageType == "view") {
						$doc->exportField($this->id);
						$doc->exportField($this->first_names);
						$doc->exportField($this->last_names);
						$doc->exportField($this->_email);
						$doc->exportField($this->password);
						$doc->exportField($this->user_level);
						$doc->exportField($this->created_at);
						$doc->exportField($this->updated_at);
					} else {
						$doc->exportField($this->id);
						$doc->exportField($this->first_names);
						$doc->exportField($this->last_names);
						$doc->exportField($this->_email);
						$doc->exportField($this->password);
						$doc->exportField($this->user_level);
						$doc->exportField($this->created_at);
						$doc->exportField($this->updated_at);
					}
					$doc->endExportRow($rowCnt);
				}
			}

			// Call Row Export server event
			if ($doc->ExportCustom)
				$this->Row_Export($recordset->fields);
			$recordset->moveNext();
		}
		if (!$doc->ExportCustom) {
			$doc->exportTableFooter();
		}
	}

